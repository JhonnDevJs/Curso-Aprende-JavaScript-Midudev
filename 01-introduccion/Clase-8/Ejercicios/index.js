// Así se puede usar console log para mostrar en consola el valor de una variable

let nombre = "Juan"

console.log(nombre)

// Tambien podemos usar console log para imprimir texto sin declararlo y almacenarlo en una variable

console.log("Hola")

// Tambien puedes unir (concatenar) texto almacenado en una variable con el texto incrustrado dentro del console log

console.log("Hola " + nombre)

// Tambien funciona con numeros

let numero = 10

console.log(numero)

console.log("El numero es " + numero)

// Existen mas metodos de console() que sirven para popositos puntuales

console.error('Error')
// ❌ Error
console.warn('Advertencia')
// ⚠️ Advertencia
console.info('Información')
// ℹ️ Información